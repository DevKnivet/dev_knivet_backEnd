package escavadorProcesso;

public class Envolvidos {
	private int id;
	private String nome;
	private String objeto_type;
	private String pivot_tipo;
	private String pivot_outros;
	private String pivot_extra_nome;
	private String link;
	private String link_api;
	private String nome_sem_filtro;
	private String envolvido_tipo;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getObjeto_type() {
		return objeto_type;
	}
	public void setObjeto_type(String objeto_type) {
		this.objeto_type = objeto_type;
	}
	public String getPivot_tipo() {
		return pivot_tipo;
	}
	public void setPivot_tipo(String pivot_tipo) {
		this.pivot_tipo = pivot_tipo;
	}
	public String getPivot_outros() {
		return pivot_outros;
	}
	public void setPivot_outros(String pivot_outros) {
		this.pivot_outros = pivot_outros;
	}
	public String getPivot_extra_nome() {
		return pivot_extra_nome;
	}
	public void setPivot_extra_nome(String pivot_extra_nome) {
		this.pivot_extra_nome = pivot_extra_nome;
	}
	public String getLink() {
		return link;
	}
	public void setLink(String link) {
		this.link = link;
	}
	public String getLink_api() {
		return link_api;
	}
	public void setLink_api(String link_api) {
		this.link_api = link_api;
	}
	public String getNome_sem_filtro() {
		return nome_sem_filtro;
	}
	public void setNome_sem_filtro(String nome_sem_filtro) {
		this.nome_sem_filtro = nome_sem_filtro;
	}
	public String getEnvolvido_tipo() {
		return envolvido_tipo;
	}
	public void setEnvolvido_tipo(String envolvido_tipo) {
		this.envolvido_tipo = envolvido_tipo;
	}
}
