package digestoData4;

public class Advogado 
{
	String advogadoId;
	String nome;
	String oab;
	String cpf;
	String uf;
	public String getAdvogadoId() {
		return advogadoId;
	}
	public void setAdvogadoId(String advogadoId) {
		this.advogadoId = advogadoId;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getOab() {
		return oab;
	}
	public void setOab(String oab) {
		this.oab = oab;
	}
	public String getCpf() {
		return cpf;
	}
	public void setCpf(String cpf) {
		this.cpf = cpf;
	}
	public String getUf() {
		return uf;
	}
	public void setUf(String uf) {
		this.uf = uf;
	}
}
