package digestoData4;

public class Movs {
	String data;
	String tipo;
	String texto;
	String nomeJuiz;
	String idInterno;
	String tipoInteligente;
	String subTipoInteligente;
	
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	public String getTexto() {
		return texto;
	}
	public void setTexto(String texto) {
		this.texto = texto;
	}
	public String getNomeJuiz() {
		return nomeJuiz;
	}
	public void setNomeJuiz(String nomeJuiz) {
		this.nomeJuiz = nomeJuiz;
	}
	public String getIdInterno() {
		return idInterno;
	}
	public void setIdInterno(String idInterno) {
		this.idInterno = idInterno;
	}
	public String getTipoInteligente() {
		return tipoInteligente;
	}
	public void setTipoInteligente(String tipoInteligente) {
		this.tipoInteligente = tipoInteligente;
	}
	public String getSubTipoInteligente() {
		return subTipoInteligente;
	}
	public void setSubTipoInteligente(String subTipoInteligente) {
		this.subTipoInteligente = subTipoInteligente;
	}
}
