package escavadorProcesso;

public class ProcessosInt {
	private int id;
	private String numero_antigo;
	private String numero_novo;
	private String link;
	private String link_api;
	private String data_movimentacoes;
	private int idUrl;
	private String slug;
	private String objeto_type;
	private int objeto_id;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNumero_antigo() {
		return numero_antigo;
	}
	public void setNumero_antigo(String numero_antigo) {
		this.numero_antigo = numero_antigo;
	}
	public String getNumero_novo() {
		return numero_novo;
	}
	public void setNumero_novo(String numero_novo) {
		this.numero_novo = numero_novo;
	}
	public String getLink() {
		return link;
	}
	public void setLink(String link) {
		this.link = link;
	}
	public String getLink_api() {
		return link_api;
	}
	public void setLink_api(String link_api) {
		this.link_api = link_api;
	}
	public String getData_movimentacoes() {
		return data_movimentacoes;
	}
	public void setData_movimentacoes(String data_movimentacoes) {
		this.data_movimentacoes = data_movimentacoes;
	}
	public int getIdUrl() {
		return idUrl;
	}
	public void setIdUrl(int idUrl) {
		this.idUrl = idUrl;
	}
	public String getSlug() {
		return slug;
	}
	public void setSlug(String slug) {
		this.slug = slug;
	}
	public String getObjeto_type() {
		return objeto_type;
	}
	public void setObjeto_type(String objeto_type) {
		this.objeto_type = objeto_type;
	}
	public int getObjeto_id() {
		return objeto_id;
	}
	public void setObjeto_id(int objeto_id) {
		this.objeto_id = objeto_id;
	}
}
