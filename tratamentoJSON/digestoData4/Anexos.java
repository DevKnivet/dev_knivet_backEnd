package digestoData4;

public class Anexos 
{
	int idAnexo;
	String endDown;
	int tipoAnexo;
	String data;
	String conteudo;
	String dataObt;
	int idMov;
	String titulo;
	
	public int getIdAnexo() {
		return idAnexo;
	}
	public void setIdAnexo(int idAnexo) {
		this.idAnexo = idAnexo;
	}
	public String getEndDown() {
		return endDown;
	}
	public void setEndDown(String endDown) {
		this.endDown = endDown;
	}
	public int getTipoAnexo() {
		return tipoAnexo;
	}
	public void setTipoAnexo(int tipoAnexo) {
		this.tipoAnexo = tipoAnexo;
	}
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	public String getConteudo() {
		return conteudo;
	}
	public void setConteudo(String conteudo) {
		this.conteudo = conteudo;
	}
	public String getDataObt() {
		return dataObt;
	}
	public void setDataObt(String dataObt) {
		this.dataObt = dataObt;
	}
	public int getIdMov() {
		return idMov;
	}
	public void setIdMov(int idMov) {
		this.idMov = idMov;
	}
	public String getTitulo() {
		return titulo;
	}
	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}
}
